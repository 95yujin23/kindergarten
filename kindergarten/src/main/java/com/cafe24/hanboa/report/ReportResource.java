package com.cafe24.hanboa.report;

public class ReportResource {
	//일지자원코드 tb_report_resource_
	
	private String reportResourceCd;
	private String groupNm;
	private String detail;
	
	public String getReportResourceCd() {
		return reportResourceCd;
	}
	public void setReportResourceCd(String reportResourceCd) {
		this.reportResourceCd = reportResourceCd;
	}
	public String getGroupNm() {
		return groupNm;
	}
	public void setGroupNm(String groupNm) {
		this.groupNm = groupNm;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	
	
}
