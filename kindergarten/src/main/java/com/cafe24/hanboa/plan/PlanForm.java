package com.cafe24.hanboa.plan;

public class PlanForm {
	private String planFormCd;
	private String planFormTitle;
	private String sheetMerge;
	private String sheetValue;
	private String sheetBorders;
	private String sheetRow;
	private String SheetCol;
	
	public String getPlanFormCd() {
		return planFormCd;
	}
	public void setPlanFormCd(String planFormCd) {
		this.planFormCd = planFormCd;
	}
	public String getPlanFormTitle() {
		return planFormTitle;
	}
	public void setPlanFormTitle(String planFormTitle) {
		this.planFormTitle = planFormTitle;
	}
	public String getSheetMerge() {
		return sheetMerge;
	}
	public void setSheetMerge(String sheetMerge) {
		this.sheetMerge = sheetMerge;
	}
	public String getSheetValue() {
		return sheetValue;
	}
	public void setSheetValue(String sheetValue) {
		this.sheetValue = sheetValue;
	}
	public String getSheetBorders() {
		return sheetBorders;
	}
	public void setSheetBorders(String sheetBorders) {
		this.sheetBorders = sheetBorders;
	}
	public String getSheetRow() {
		return sheetRow;
	}
	public void setSheetRow(String sheetRow) {
		this.sheetRow = sheetRow;
	}
	public String getSheetCol() {
		return SheetCol;
	}
	public void setSheetCol(String sheetCol) {
		SheetCol = sheetCol;
	}
}
